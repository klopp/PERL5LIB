package Things::Db;

# ------------------------------------------------------------------------------
use strict;
use warnings;

# ------------------------------------------------------------------------------
use Things::AutoMod;

# ------------------------------------------------------------------------------
our $VERSION = 'v1.00';

# ------------------------------------------------------------------------------
sub new
{
    my ( undef, $target, @params ) = @_;
    return Things::AutoMod->new( 'Db::' . $target, @params );
}

# ------------------------------------------------------------------------------
sub DESTROY
{
    my ($self) = @_;
    return $self->DESTROY;
}

# ------------------------------------------------------------------------------
1;
__END__

=head1 SYNOPSIS

    my $db = Things::Db->new( 'Mysql', ... );

=cut

