package Things::Out;

# ------------------------------------------------------------------------------
use strict;
use warnings;

# ------------------------------------------------------------------------------
use English qw/-no_match_vars/;

# ------------------------------------------------------------------------------
use base qw/Exporter/;
our @EXPORT  = qw/puts fputs perror strerror strerr/;
our $VERSION = 'v1.0';

# ------------------------------------------------------------------------------
BEGIN {
    *{strerr} = \&strerror;
}

# ------------------------------------------------------------------------------
sub puts
{
    my ( $fmt, @args ) = @_;
    return fputs( *{STDOUT}, $fmt, @args );
}

# ------------------------------------------------------------------------------
sub fputs
{
    my ( $fh, $fmt, @args ) = @_;
    return print {$fh} sprintf "$fmt\n", @args;
}

# ------------------------------------------------------------------------------
sub strerror
{
    my ($msg) = @_;
    defined $msg and return sprintf "%s: %s\n", $msg, $ERRNO;;
    return $ERRNO;
}

# ------------------------------------------------------------------------------
sub perror
{
    return print STDERR strerror(@_);
}

# ------------------------------------------------------------------------------
1;
